function M = reduce_L(L0)
  % This function add leadfield matrix L10, L20_32, L20 to struct M
  % L0 is based on 10-5 electrode placement system with 108 labels in 'full_label'
  % L10 is based on reduce_1010
  % L20_32 is based on reduce_L1020_32
  % L20 is based on reduce_L1020
  % INPUT: L0
  % OUTPUT: M.L10
  %       : M.L20_32
  %       : M.L20
  %       : M.L10_ind, column index of L10 indices from L10
  %       : M.L20_32_ind, column index of L10 indices from L20_32
  %       : M.L20_ind, column index of L10 indices from L20
  % Written by: PARINTHORN MANOMAISAOWAPAK


full_label = {'AF3','AF4','AF7','AF8','AFF1h', ...
    'AFF2h','AFF5h','AFF6h','AFF7h','AFF8h', ...
    'AFp1','AFp2','AFz','C1','C2','C3','C4', ...
    'C5','C6','CCP1h','CCP2h','CCP3h','CCP4h', ...
    'CCP5h','CCP6h','CP1','CP2','CP3','CP4','CP5', ...
    'CP6','CPP1h','CPP2h','CPP3h','CPP4h','CPP5h', ...
    'CPP6h','CPz','Cz','F1','F2','F3','F4','F5','F6', ...
    'F7','F8','FC1','FC2','FC3','FC4','FC5','FC6','FCC1h' ...
    ,'FCC2h','FCC3h','FCC4h','FCC5h','FCC6h','FCz','FFC1h', ...
    'FFC2h','FFC3h','FFC4h','FFC5h','FFC6h','FFT7h','FFT8h', ...
    'FT7','FT8','FTT7h','FTT8h','Fp1','Fp2','Fpz','Fz','O1', ...
    'O2','OI1h','OI2h','Oz','P1','P2','P3','P4','P5','P6', ...
    'P7','P8','PO1','PO2','PO3','PO4','PO7','PO8','POz','PPO3h', ...
    'PPO4h','PPO7h','Pz','T7','T8','TP7','TP8','TPP7h','TPP8h', ...
    'TTP7h','TTP8h'};
M.reduce_1010 = {'Fp1','Fp2','F7','F3','Fz','F4','F8','T7','C3', ...
    'Cz','C4','T8','P7','P3','Pz','P4','P8','O1','O2','Fpz', ...
    'AF7','AF3','AFz','AF4','AF8','F9','F5','F1','F2','F6','F10', ...
    'FT9','FT7','FC5','FC3','FC1','FCz','FC2','FC4','FC6','FT8', ...
    'FT10','T9','C5','C1','C2','C6','T10','TP9','TP7','CP5','CP3', ...
    'CP1','CPz','CP2','CP4','CP6','TP8','TP10','P9','P5','P1','P2','P6', ...
    'P10','PO7','PO3','POz','PO4','PO8','Oz'};
M.reduce_1020 = {'Fp1','Fp2','F7','F3','Fz','F4','F8','T7','C3','Cz','C4', ...
    'T8','P7','P3','Pz','P4','P8','O1','O2'};
M.reduce_1020_32 = {'F7','T7','P7', ...
 'Fp1', 'FC5', 'CP5', 'O1', ...
 'AF3', 'F3', 'C3','P3','PO3', ...
 'FC1', 'CMS', 'CP1', 'Oz', ...
 'Fz', 'Cz', 'Pz', 'FC2', 'DRL', 'CP2', 'Fp2', ...
 'AF4', 'F4', 'C4', 'P4', 'PO4', 'O2', 'FC6', 'CP6', ...
 'F8', 'T8', 'P8'}; %Riera, Alejandro. (2012). Computational Intelligence Techniques for Electro-Physiological Data Analysis.

M.L10_ind = find(ismember(full_label,M.reduce_1010));
M.L20_ind = find(ismember(full_label,M.reduce_1020));
M.L20_ind_32 = find(ismember(full_label,M.reduce_1020_32));

M.L10 = L0(M.L10_ind,:);
M.L20 = L0(M.L20_ind,:);
M.L20_32 = L0(M.L20_ind_32,:);
end
